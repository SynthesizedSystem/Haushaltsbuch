namespace Haushaltsbuch
{
    /// <summary>
    /// An empty window that can be used on its own or navigated to within a Frame.
    /// </summary>
    public sealed partial class MainWindow : Window
    {
        public MainWindow()
        {
            InitializeComponent();
            ExtendsContentIntoTitleBar = true;

            IntPtr window_handle = WinRT.Interop.WindowNative.GetWindowHandle(this);
            WindowId window_id = Win32Interop.GetWindowIdFromWindow(window_handle);
            AppWindow app_window = AppWindow.GetFromWindowId(window_id);

            app_window.Resize(new SizeInt32(1000, 600));
        }
    }
}
